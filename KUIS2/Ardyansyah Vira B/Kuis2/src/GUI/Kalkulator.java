    private void pilihanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pilihanActionPerformed
        // TODO add your handling code here:
       a = Double.parseDouble(a1.getText());
       b = Double.parseDouble(a2.getText());
      
if(pilihan.getSelectedItem().equals("+")){
   
       c = a+b;

}else if(pilihan.getSelectedItem().equals("-")){
   
       c = a-b;
   
}else if(pilihan.getSelectedItem().equals(":")){
   
       c=a/b;

}else if(pilihan.getSelectedItem().equals("x")){
   
       c=a*b;
   
}

       hasil.setText(c + "");
    }//GEN-LAST:event_pilihanActionPerformed

