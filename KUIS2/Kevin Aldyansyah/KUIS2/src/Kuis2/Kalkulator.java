    private void jButtonNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonNextActionPerformed
        buka_koneksi();
        String sqlkode = "Insert into record_data (angka1, angka2, operator, hasil) " 
                + "values ('" + this.jTFAngka1.getText() + "'," 
                + "'" + this.jTFAngka2.getText() + "'," 
                + "'" + this.jCBOperator.getSelectedItem().toString() + "'," 
                + "'" + this.jTFHasil.getText()+ "')";
        
        try {
            PreparedStatement mStatement = koneksi.prepareStatement(sqlkode);
            mStatement.executeUpdate();
            mStatement.close();
            JOptionPane.showMessageDialog(this, "Data Berhasil Ditambah");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Terjadi Kesalahan " + ex.getMessage());
        }
        new RecordData().setVisible(true);
    }//GEN-LAST:event_jButtonNextActionPerformed

