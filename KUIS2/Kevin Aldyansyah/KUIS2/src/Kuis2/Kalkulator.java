    private void jCBOperatorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCBOperatorActionPerformed
        // TODO add your handling code here:
        angka1 = Double.parseDouble(jTFAngka1.getText());
        angka2 = Double.parseDouble(jTFAngka2.getText());
        
        if (jCBOperator.getSelectedItem().equals("+")) {
            hasilHitung = angka1 + angka2;
            String hasilTambah = String.valueOf(hasilHitung);
            jTFHasil.setText(hasilTambah);
        }else if (jCBOperator.getSelectedItem().equals("-")) {
            hasilHitung = angka1 - angka2;
            String hasilKurang = String.valueOf(hasilHitung);
            jTFHasil.setText(hasilKurang);
        }else if(jCBOperator.getSelectedItem().equals("*")){
            hasilHitung = angka1 * angka2;
            String hasilKali = String.valueOf(hasilHitung);
            jTFHasil.setText(hasilKali);
        }else if (jCBOperator.getSelectedItem().equals("/")) {
            hasilHitung = angka1 / angka2;
            String hasilBagi = String.valueOf(hasilHitung);
            jTFHasil.setText(hasilBagi);
        }
    }//GEN-LAST:event_jCBOperatorActionPerformed

